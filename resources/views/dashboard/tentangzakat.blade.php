							UU tersebut juga menjelaskan tentang zakat fitrah, yaitu sejumlah bahan pokok yang dikeluarkan pada bulan Ramadan oleh setiap Muslim bagi dirinya dan bagi orang yang ditanggungnya, yang memiliki kewajiban makan pokok untuk sehari pada hari raya Idul Fitri.</p>

